/*创建总得分矩形*/
function drawShape(deviceWidth,score){
    var xWidth = deviceWidth-2;
    var canvas = document.getElementById('canvas');
    if(canvas.getContext){
        var ctx = canvas.getContext("2d");

        /*低*/
        var lowScore = ctx.createLinearGradient(1,0,xWidth,40);
        lowScore.addColorStop(0, '#00CB01');
        lowScore.addColorStop(1, '#00CB01');

        /*高*/
        var middleScore = ctx.createLinearGradient(1,0,xWidth,40);
        middleScore.addColorStop(0, '#FD6601');
        middleScore.addColorStop(1, '#FD6601');

        /*绘制矩形*/
        ctx.fillStyle = lowScore;
        ctx.fillRect(1,30,xWidth/450*160,40);
        ctx.fillStyle = middleScore;
        ctx.fillRect(xWidth/450*160,30,xWidth,40);

        /*绘制数值*/
        ctx.font= 'Bold 15px Sans-Serif';
        ctx.fillStyle="#000000";
        ctx.fillText('90',0,15);
        var font160 = xWidth/450*160-15;
        ctx.fillText('160',font160,15);
        var fontWidth = xWidth-30;
        ctx.fillText('450',fontWidth,15);

        /*绘制文本低*/
        var lhLocation = xWidth/450*160/2-10;
        ctx.fillStyle="#FFFFFF";
        ctx.font= 'Bold 15px Sans-Serif';
        ctx.fillText('低',lhLocation,55);
        /*绘制文本欠佳*/
        var qjLocation = xWidth/450*450/2+50;
        ctx.fillText('高',qjLocation,55);

        /*绘制总分数线条*/
        if(score==90){
            var scoreLine = xWidth/450*score;
            scoreLine = scoreLine-scoreLine
        }else{
            var scoreLine = xWidth/450*score;
        }
        /*得分结果绘制*/
        if(score==90){
            /*score==90 线条绘制*/
            ctx.beginPath();
            ctx.strokeStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine,70);
            ctx.lineTo(scoreLine+15,90);
            ctx.stroke();
            /*得分score==90 三角形绘制*/
            ctx.beginPath();
            ctx.fillStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine+15,95);
            ctx.lineTo(scoreLine+15-5,90);
            ctx.lineTo(scoreLine+15+5,90);
            ctx.fill();
            ctx.closePath();
            /*得分score==90 分数绘制*/
            ctx.beginPath();
            ctx.fillStyle="#000";
            ctx.font= 'Bold 15px Sans-Serif';
            ctx.fillText(""+score+"",scoreLine+24-14,110);
            ctx.fill();
            ctx.closePath();
        }else if(score>90 && score<410){
            /*正常绘制分数线条*/
            ctx.beginPath();
            ctx.strokeStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine,70);
            ctx.lineTo(scoreLine,90);
            ctx.stroke();
            /*绘制三角形*/
            ctx.beginPath();
            ctx.fillStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine,95);
            ctx.lineTo(scoreLine-5,90);
            ctx.lineTo(scoreLine+5,90);
            ctx.closePath();
            ctx.fill();
            /*score>90 && score<440 分数绘制*/
            ctx.beginPath();
            ctx.fillStyle="#000000";
            ctx.font= 'Bold 15px Sans-Serif';
            ctx.fillText(""+score+"",scoreLine-14,110);
            ctx.fill();
            ctx.closePath();
        }else if(score>=410 && score <=450){
            /*score>=440 && score <=450 线条绘制*/
            ctx.beginPath();
            ctx.strokeStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine,70);
            ctx.lineTo(scoreLine-15,90);
            ctx.stroke();
            /*score>=440 && score <=450 三角形绘制*/
            ctx.beginPath();
            ctx.fillStyle="rgb(90,137,193)";
            ctx.moveTo(scoreLine-15,95);
            ctx.lineTo(scoreLine-15-5,90);
            ctx.lineTo(scoreLine-15+5,90);
            ctx.fill();
            ctx.closePath();
            /*score>=440 && score <=450 分数绘制*/
            ctx.beginPath();
            ctx.fillStyle="#000000";
            ctx.font= 'Bold 15px Sans-Serif';
            ctx.fillText(""+score+"",scoreLine-43,110);
            ctx.fill();
            ctx.closePath();
        }else{
            alert("errorScore");
        }
    }
}

